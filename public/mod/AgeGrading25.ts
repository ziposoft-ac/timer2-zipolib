export function getAgeRecordSeconds(
    tbl:AgeGradingTable,
    distance: number, /* distance  */
    units: DistanceUnits/* km or miles */,
    age:number): number | false {


    let colIdx = getColumnIdx(distance, units);
    if (colIdx >= 0) {
        let wr=tbl.wr[colIdx];
        let factor=tbl.ages[age][colIdx];
        return wr/factor;
    }
    let dRow=t.distKM;
    if (units === DistanceUnits.miles)
        dRow=t.distMiles;
    if (distance < dRow[0])
        return false;
    if (distance >=dRow.at(-1) )
        return false;
    for (let i = 1; i < dRow.length; i++) {
        let d2 = dRow[i];
        if (d2 > distance) {
            /* estimate what the WR time would be */
            let d1 = dRow[i - 1];
            let t1 = tbl.wr[i - 1];
            let t2 = tbl.wr[i ];
            let wr = ((t2 - t1) * (distance - d1) / (d2 - d1)) + t1;
            let af1=tbl.ages[age][i-1];
            let af2=tbl.ages[age][i];
            let af = ((af2 - af1) * (distance - d1) / (d2 - d1)) + af1;

            return wr/af;
        }
    }
    return false;
}

export function getColumnIdx(distance: number/* distance in kilometers */,
                      units: DistanceUnits/* km or miles */): number {
    let row = t.distMiles
    if (units === DistanceUnits.km)
        row = t.distKM;
    if (units === DistanceUnits.meters)
    {
        distance=distance/1000;
        row = t.distKM;

    }
    for (let i = 0; i < t.distKM.length; i++) {
        if (row[i] === distance)
            return i;

    }
    return -1;
}


