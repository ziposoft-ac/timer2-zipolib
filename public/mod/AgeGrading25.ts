export function getGrade(
    gender: Gender /* M/F */,
    age: number/* Age in years 5-100 */,
    distance: number,
    units: DistanceUnits/* km or miles */,
    time: string): number {
    let secondsIn = timeStrToSec(time);
    return getGradeFromSeconds(gender, age, distance, units, secondsIn)


}

export function getGradeFromSeconds(
    gender: Gender /* M/F */,
    age: number/* Age in years 5-100 */,
    distance: number,
    units: DistanceUnits/* km or miles */,
    secondsIn: number): number {
    age = Number(age);
    distance = Number(distance);
    secondsIn = Number(secondsIn);

    if (!secondsIn)
        return 0;
    if (!age)
        return 0;
    if (units == DistanceUnits.meters) {
        //TODO support meters properly
        units = DistanceUnits.km;
        distance = distance / 1000;
    }
    let tbl=getTable(gender);
    if(!tbl)
        return 0;
    let wrSec = getAgeRecordSeconds(tbl,  distance, units,age);
    if (!wrSec)
        return 0;
    //console.log("age standard=",wrSec);
    return wrSec / secondsIn * 100;


}

export function secToTimeStr(sec: number): string {
    if (!sec)
        return "";
    sec = Number(sec);

    sec = Math.round(sec);
    let str = "";
    let hours: number = Math.floor(sec / (60 * 60));
    sec -= hours * 60 * 60;
    let mins: number = Math.floor(sec / (60));
    sec -= mins * 60;

    if (hours) {
        str = hours + ":" + ("0" + mins).slice(-2);
    } else
        str = mins.toString();
    str += ":" + ("0" + sec).slice(-2);
    return str;
}

export function timeStrToSec(time: string): number {
    let sec = time.split('.');
    let p = time.split(':');
    let p0 = parseInt(p[0]);
    let p1 = parseInt(p[1]);

    let secs = 0;
    if (p.length == 2) {
        secs = (p0 * 60 + p1);
    }
    if (p.length == 3) {
        let p2 = parseInt(p[2]);
        secs = (p0 * 60 * 60 + p1 * 60 + p2);
    }
    return secs;
}

