export const KM_IN_MILES = 1.60934;

export enum Gender {
    Male = 'M',
    Female = 'F',
    Other = 'O',
    Unknown = 'U',
    NotSet = '?',
};

export enum DistanceUnits {
    miles = "miles",
    meters = "meters",
    km = "km"
};

